static void backlight_init(void)
{
    ledc_timer_config_t timer = {
        .speed_mode      = LEDC_LOW_SPEED_MODE,
        .timer_num       = LEDC_TIMER_0,
        .duty_resolution = LEDC_TIMER_13_BIT,
        .freq_hz         = 5000,
        .clk_cfg         = LEDC_AUTO_CLK,
    };
    ledc_timer_config(&timer);

    ledc_channel_config_t channel = {
        .speed_mode = LEDC_LOW_SPEED_MODE,
        .channel    = LEDC_CHANNEL_0,
        .timer_sel  = LEDC_TIMER_0,
        .intr_type  = LEDC_INTR_DISABLE,
        .gpio_num   = LCD_BACKLIGHT_PIN,
        .duty       = (1 << 13) - 1, // full brightness
        .hpoint     = 0,
    };
    ledc_channel_config(&channel);
}

// ---- Public drawing API ----

void display_init(void)
{
    spi_set_pins_output();
    st7701s_init_sequence();
    rgb_panel_init();
    backlight_init();
    display_clear(COLOR_BLACK);
}

void display_clear(uint16_t color)
{
    if (!s_fb) return;
    uint32_t count = LCD_WIDTH * LCD_HEIGHT;
    for (uint32_t i = 0; i < count; i++) s_fb[i] = color;
}

void display_fill_rect(int x, int y, int w, int h, uint16_t color)
{
    if (!s_fb) return;
    for (int row = y; row < y + h && row < LCD_HEIGHT; row++) {
        for (int col = x; col < x + w && col < LCD_WIDTH; col++) {
            s_fb[row * LCD_WIDTH + col] = color;
        }
    }
}

void display_draw_char(int x, int y, char c, uint16_t fg, uint16_t bg, int scale)
{
    if (!s_fb || c < 32 || c > 126) return;
    const uint8_t *glyph = s_font8x8[(uint8_t)c - 32];
    for (int row = 0; row < 8; row++) {
        uint8_t line = glyph[row];
        for (int col = 0; col < 8; col++) {
            uint16_t color = (line & (1 << col)) ? fg : bg;
            display_fill_rect(x + col * scale, y + row * scale, scale, scale, color);
        }
    }
}

void display_draw_string(int x, int y, const char *s, uint16_t fg, uint16_t bg, int scale)
{
    int cx = x;
    while (*s) {
        display_draw_char(cx, y, *s, fg, bg, scale);
        cx += 8 * scale;
        s++;
    }
}

