// ---- ST7701S init via software SPI ----

static void spi_set_pins_output(void)
{
    gpio_config_t cfg = {
        .pin_bit_mask = (1ULL << LCD_SPI_CLK) | (1ULL << LCD_SPI_MOSI) | (1ULL << LCD_SPI_CS),
        .mode         = GPIO_MODE_OUTPUT,
        .pull_up_en   = GPIO_PULLUP_DISABLE,
        .pull_down_en = GPIO_PULLDOWN_DISABLE,
        .intr_type    = GPIO_INTR_DISABLE,
    };
    gpio_config(&cfg);
    gpio_set_level(LCD_SPI_CS,  1);
    gpio_set_level(LCD_SPI_CLK, 0);
}

static void spi_write_byte(uint8_t b)
{
    for (int i = 7; i >= 0; i--) {
        gpio_set_level(LCD_SPI_CLK, 0);
        gpio_set_level(LCD_SPI_MOSI, (b >> i) & 1);
        gpio_set_level(LCD_SPI_CLK, 1);
    }
}

static void lcd_send_cmd(uint8_t cmd, const uint8_t *data, int len)
{
    gpio_set_level(LCD_SPI_CS, 0);
    spi_write_byte(cmd);
    for (int i = 0; i < len; i++) spi_write_byte(data[i]);
    gpio_set_level(LCD_SPI_CS, 1);
}

static void st7701s_init_sequence(void)
{
    // CMD2 BK0 enable
    lcd_send_cmd(0xFF, (uint8_t[]){0x77,0x01,0x00,0x00,0x10}, 5);
    lcd_send_cmd(0xC0, (uint8_t[]){0x3B,0x00}, 2);           // LNESET
    lcd_send_cmd(0xC1, (uint8_t[]){0x0D,0x02}, 2);           // PORCTRL
    lcd_send_cmd(0xC2, (uint8_t[]){0x21,0x08}, 2);           // INVSET
    lcd_send_cmd(0xCD, (uint8_t[]){0x00}, 1);                 // disable MDT
    lcd_send_cmd(0xB0, (uint8_t[]){0x00,0x11,0x18,0x0E,0x11,0x06,0x07,0x08,
                                    0x07,0x22,0x04,0x12,0x0F,0xAA,0x31,0x18}, 16); // PVGAM
    lcd_send_cmd(0xB1, (uint8_t[]){0x00,0x11,0x19,0x0E,0x12,0x07,0x08,0x08,
                                    0x08,0x22,0x04,0x11,0x11,0xA9,0x32,0x18}, 16); // NVGAM

    // CMD2 BK1 enable
    lcd_send_cmd(0xFF, (uint8_t[]){0x77,0x01,0x00,0x00,0x11}, 5);
    lcd_send_cmd(0xB0, (uint8_t[]){0x60}, 1); // VOPOFF
    lcd_send_cmd(0xB1, (uint8_t[]){0x32}, 1); // VCOM
    lcd_send_cmd(0xB2, (uint8_t[]){0x07}, 1); // VGH
    lcd_send_cmd(0xB3, (uint8_t[]){0x80}, 1); // TESTCMD
    lcd_send_cmd(0xB5, (uint8_t[]){0x49}, 1); // VGL
    lcd_send_cmd(0xB7, (uint8_t[]){0x85}, 1); // PWCTRL1
    lcd_send_cmd(0xB8, (uint8_t[]){0x21}, 1); // PWCTRL2
    lcd_send_cmd(0xC1, (uint8_t[]){0x78}, 1); // PCLK2
    lcd_send_cmd(0xC2, (uint8_t[]){0x78}, 1); // PCLK3
    vTaskDelay(pdMS_TO_TICKS(20));

    lcd_send_cmd(0xE0, (uint8_t[]){0x00,0x1B,0x02}, 3);
    lcd_send_cmd(0xE1, (uint8_t[]){0x08,0xA0,0x00,0x00,0x07,0xA0,0x00,0x00,0x00,0x44,0x44}, 11);
    lcd_send_cmd(0xE2, (uint8_t[]){0x11,0x11,0x44,0x44,0xED,0xA0,0x00,0x00,0xEC,0xA0,0x00,0x00}, 12);
    lcd_send_cmd(0xE3, (uint8_t[]){0x00,0x00,0x11,0x11}, 4);
    lcd_send_cmd(0xE4, (uint8_t[]){0x44,0x44}, 2);
    lcd_send_cmd(0xE5, (uint8_t[]){0x0A,0xE9,0xD8,0xA0,0x0C,0xEB,0xD8,0xA0,
                                    0x0E,0xED,0xD8,0xA0,0x10,0xEF,0xD8,0xA0}, 16);
    lcd_send_cmd(0xE6, (uint8_t[]){0x00,0x00,0x11,0x11}, 4);
    lcd_send_cmd(0xE7, (uint8_t[]){0x44,0x44}, 2);
    lcd_send_cmd(0xE8, (uint8_t[]){0x09,0xE8,0xD8,0xA0,0x0B,0xEA,0xD8,0xA0,
                                    0x0D,0xEC,0xD8,0xA0,0x0F,0xEE,0xD8,0xA0}, 16);
    lcd_send_cmd(0xEB, (uint8_t[]){0x02,0x00,0xE4,0xE4,0x88,0x00,0x40}, 7);
    lcd_send_cmd(0xEC, (uint8_t[]){0x3C,0x00}, 2);
    lcd_send_cmd(0xED, (uint8_t[]){0xAB,0x89,0x76,0x54,0x02,0xFF,0xFF,0xFF,
                                    0xFF,0xFF,0xFF,0x20,0x45,0x67,0x98,0xBA}, 16);

    // CMD2 disable, back to user commands
    lcd_send_cmd(0xFF, (uint8_t[]){0x77,0x01,0x00,0x00,0x00}, 5);
    lcd_send_cmd(0x3A, (uint8_t[]){0x55}, 1); // COLMOD: 16-bit RGB565
    lcd_send_cmd(0x36, (uint8_t[]){0x00}, 1); // MADCTL
    lcd_send_cmd(0x11, NULL, 0);               // SLEEP OUT
    vTaskDelay(pdMS_TO_TICKS(120));
    lcd_send_cmd(0x29, NULL, 0);               // DISPLAY ON
    vTaskDelay(pdMS_TO_TICKS(20));
    ESP_LOGI(TAG, "ST7701S init done");
}

// ---- RGB panel ----

static esp_lcd_panel_handle_t s_panel = NULL;
static uint16_t *s_fb = NULL;   // framebuffer pointer in PSRAM

static void rgb_panel_init(void)
{
    esp_lcd_rgb_panel_config_t cfg = {
        .clk_src     = LCD_CLK_SRC_DEFAULT,
        .timings = {
            .pclk_hz            = LCD_PCLK_HZ,
            .h_res              = LCD_WIDTH,
            .v_res              = LCD_HEIGHT,
            .hsync_pulse_width  = 8,
            .hsync_back_porch   = 20,
            .hsync_front_porch  = 10,
            .vsync_pulse_width  = 8,
            .vsync_back_porch   = 10,
            .vsync_front_porch  = 10,
            .flags.pclk_active_neg = 0,
        },
        .data_width  = 16,
        .num_fbs     = 1,
        .psram_trans_align = 64,
        .hsync_gpio_num = LCD_HSYNC_PIN,
        .vsync_gpio_num = LCD_VSYNC_PIN,
        .de_gpio_num    = LCD_DE_PIN,
        .pclk_gpio_num  = LCD_PCLK_PIN,
        .data_gpio_nums = {
            LCD_B0, LCD_B1, LCD_B2, LCD_B3, LCD_B4,       // D[0:4]  = B[0:4]
            LCD_G0, LCD_G1, LCD_G2, LCD_G3, LCD_G4, LCD_G5, // D[5:10] = G[0:5]
            LCD_R0, LCD_R1, LCD_R2, LCD_R3, LCD_R4,       // D[11:15]= R[0:4]
        },
        .disp_gpio_num = -1,
        .flags.fb_in_psram = 1,
        .flags.relax_on_idle = 0,
    };

    ESP_ERROR_CHECK(esp_lcd_new_rgb_panel(&cfg, &s_panel));
    ESP_ERROR_CHECK(esp_lcd_panel_reset(s_panel));
    ESP_ERROR_CHECK(esp_lcd_panel_init(s_panel));

    ESP_ERROR_CHECK(esp_lcd_rgb_panel_get_frame_buffer(s_panel, 1, (void **)&s_fb));
    ESP_LOGI(TAG, "RGB panel init done, fb=%p", s_fb);
}

