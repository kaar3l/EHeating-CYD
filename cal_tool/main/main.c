/* ── XPT2046 raw read ────────────────────────────────────────────────────── */
static uint16_t tch_ch(uint8_t cmd)
{
    spi_transaction_t t = {
        .length=24, .flags=SPI_TRANS_USE_TXDATA|SPI_TRANS_USE_RXDATA,
    };
    t.tx_data[0]=cmd; t.tx_data[1]=0; t.tx_data[2]=0;
    spi_device_polling_transmit(s_tch, &t);
    return ((uint16_t)t.rx_data[1]<<8 | t.rx_data[2]) >> 3;
}

/* Returns true if screen is touched; rx/ry = averaged raw ADC values */
static bool tch_raw(int *rx, int *ry)
{
    if (gpio_get_level(TCH_IRQ) != 0) return false;
    int32_t ax=0, ay=0;
    for (int i=0; i<8; i++) { ax += tch_ch(0xD0); ay += tch_ch(0x90); }
    *rx = (int)(ax/8);
    *ry = (int)(ay/8);
    return true;
}

/* ── Main ────────────────────────────────────────────────────────────────── */
