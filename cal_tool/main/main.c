    /* Top-right */
    fill_rect(300, 0, 20, 1, ORANGE); fill_rect(319, 0, 1, 20, ORANGE);
    /* Bottom-right */
    fill_rect(300,239,20,1,ORANGE); fill_rect(319,220,1,20,ORANGE);
