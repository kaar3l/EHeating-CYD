    draw_str(4, 4,  "CYD Touch Calibration Tool", CYAN, BLACK, 1);
    fill_rect(0, 14, LCD_W, 1, GRAY);
    draw_str(4, 18, "Tap corner, note X+Y values", WHITE, BLACK, 1);
    draw_str(4, 28, "Tell 8 values to Claude", GRAY, BLACK, 1);
    fill_rect(0, 38, LCD_W, 1, GRAY);

